#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	srclen;

	srclen = ft_strlen(src);
	if (size == 0)
		return (srclen);
	if (srclen + 1 <= size)
	{
		ft_memcpy(dst, src, srclen + 1);
	}
	else
	{
		ft_memcpy(dst, src, size - 1);
		dst[size - 1] = 0;
	}
	return (srclen);
}
