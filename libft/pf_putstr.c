#include "libft.h"

int	pf_putstr(char *s)
{
	size_t	len;

	if (s == NULL)
	{
		len = 6;
		write(1, "(null)", 6);
	}
	else
	{
		len = ft_strlen(s);
		write(1, s, len);
	}
	return (len);
}
